import os
from tqdm import tqdm
def find_folders_with_subfolder(root_path, keys = [], path_keys = [] ,excs = [] ,path_excs =[]):
    """
    Find all folders in the root_path that contain a subfolder with the name subfolder_name.
    """
    res = []

    # Walk through the directory
    for dirpath, dirnames, filenames in os.walk(root_path):
        # Check if the subfolder_name is in the list of directories
        if os.path.basename(dirpath).lower() == 'image':
            for f in filenames:
                res.append(os.path.join(dirpath,f))
    return res


def get_data(path):
    txts = []
    data = find_folders_with_subfolder(path,keys=['image'],path_keys=['train'])
    for d in data:
        depth = d.replace('.png','.exr').replace('/image/','/world_depth/')
        if os.path.isfile(depth):
            txts.append([d,depth])
    return txts


def process_data(txt,output='default.txt'):
    with open(output,'w') as f:
        for line in txt:
	        f.write(str(line[0])+' '+ str(line[1])+'\n')


