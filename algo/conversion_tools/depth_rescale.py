from file_utils import jhelp_file,read,write
import numpy as np 

depths = jhelp_file('/Users/qhong/Downloads/worlddepth')
for depth in depths:
    data = read(depth)/100
    data[np.where(data>60)] = 0
    d = data[...,0]
    d = (d - d.min()) / (d.max() - d.min())
    data[...,-1] = d
    write(depth.replace('worlddepth','worlddepth_rescaled'),data)